# Enkel variabel
meddelande = "Jag lär mig Python Crash Course!"
print(meddelande)

# Ändra variabeln (Variabler kan variera!)
meddelande = "Nu har jag ändrat värdet på variabeln."
print(meddelande)

# Strings (Text) med stora/små bokstäver
namn = "sean brinkenhof"
print(namn.title())
print(namn.upper())
print(namn.lower())

# f-strings (Kombinera text och variabler)
fullstandigt_namn = f"Hej, mitt namn är {namn.title()}!"
print(fullstandigt_namn)
